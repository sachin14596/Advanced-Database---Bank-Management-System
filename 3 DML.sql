----------------------------------------------------------------------------------------------------------------------------------------------------------------
DECLARE @customerIndex INT = 1;
DECLARE @transactionIndex INT = 1;
DECLARE @maxCustomers INT = 100;
DECLARE @minTransactionsPerCustomer INT = 3;
DECLARE @maxTransactionsPerCustomer INT = 5;

WHILE @customerIndex <= @maxCustomers
BEGIN
    DECLARE @numTransactions INT = FLOOR(RAND() * (@maxTransactionsPerCustomer - @minTransactionsPerCustomer + 1)) + @minTransactionsPerCustomer;
    DECLARE @customerID NVARCHAR(8) = 'C' + RIGHT('00000' + CAST(@customerIndex AS NVARCHAR(5)), 6);
    DECLARE @accNo INT = 10000000 + @customerIndex;
    
    DECLARE @i INT = 1;
    WHILE @i <= @numTransactions
    BEGIN
        DECLARE @amount FLOAT = FLOOR(RAND() * 299999) + 1;
        DECLARE @transactionType NVARCHAR(15) = CASE WHEN RAND() > 0.5 THEN 'Deposit' ELSE 'Withdrawal' END;
        DECLARE @transactionDate DATE = DATEADD(DAY, @transactionIndex, '2023-01-01');
        
        DECLARE @transactionID NVARCHAR(8) = 'T' + RIGHT('000000' + CAST(@transactionIndex AS NVARCHAR(6)), 6);

        INSERT INTO transactions (customer_ID, acc_no, transaction_ID, amount, transaction_date, transaction_type)
        VALUES (@customerID, @accNo, @transactionID, @amount, @transactionDate, @transactionType);

        SET @transactionIndex = @transactionIndex + 1;
        SET @i = @i + 1;
    END

    SET @customerIndex = @customerIndex + 1;
END



